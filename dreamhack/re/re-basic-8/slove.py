target_bytes = [
    0xAC, 0xF3, 0x0C, 0x25, 0xA3, 0x10, 0xB7, 0x25, 0x16, 0xC6, 
    0xB7, 0xBC, 0x07, 0x25, 0x02, 0xD5, 0xC6, 0x11, 0x07, 0xC5
]

a1 = ""
for target in target_bytes:
    for possible_value in range(256):
        if (-5 * possible_value) & 0xFF == target:
            a1+=chr(possible_value)
            break

print(a1)
